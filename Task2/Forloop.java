package Task2;

import java.util.Scanner;

public class Forloop {

	public static void main(String[] args) {
		
		System.out.println("Enter No of Times to be Printed : ");
		
		Scanner s = new Scanner(System.in);
		
		int a = s.nextInt();
		
		int i;
		
		for (i=1;i<=a;i++) {
			
			System.out.print("Akhil  " );
			s.close();
		}
		
	}

}
